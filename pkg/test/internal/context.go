package internal

import (
	"istio.io/istio/pkg/test/environment"
)

// TestContext is an interface that provides the ambient context to internal code.
type TestContext interface {
	// TestID is the id of the test. This is supplied by the test author.
	TestID() string

	// RunID is the id of the current test run. This is generated by the runtime.
	RunID() string

	// Environment returns the ambient testing environment. This must be called from tests, while the
	// Run() method is executing.
	Environment() environment.Interface

	// CreateTmpDirectory creates a new temporary directory for storing local, temporary files.
	CreateTmpDirectory(name string) (string, error)
}
